import { copyFileSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { createHash } from "node:crypto";
import { dirname, extname, join, basename } from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = dirname(dirname(fileURLToPath(import.meta.url)));
const publicDir = join(rootDir, "public");
const sourceDir = join(publicDir, "pwa");
const generatedDir = join(sourceDir, "generated");
const manifestFile = join(rootDir, "shared", "generated", "pwa-assets.ts");

const assets = [
  { source: "icon.svg", path: "/pwa/icon.svg" },
  { source: "icon-192.png", path: "/pwa/icon-192.png" },
  { source: "icon-512.png", path: "/pwa/icon-512.png" },
  { source: "icon-maskable-512.png", path: "/pwa/icon-maskable-512.png" },
  { source: "apple-touch-icon.png", path: "/pwa/apple-touch-icon.png" },
];

function contentHash(buffer) {
  return createHash("sha256").update(buffer).digest("hex").slice(0, 12);
}

rmSync(generatedDir, { force: true, recursive: true });
mkdirSync(generatedDir, { recursive: true });

const pwaAssetManifest = {};

for (const asset of assets) {
  const sourceFile = join(sourceDir, asset.source);
  const content = readFileSync(sourceFile);
  const extension = extname(asset.source);
  const name = basename(asset.source, extension);
  const targetName = `${name}.${contentHash(content)}${extension}`;
  const targetPath = `/pwa/generated/${targetName}`;

  copyFileSync(sourceFile, join(generatedDir, targetName));
  pwaAssetManifest[asset.path] = targetPath;
}

const pwaAssetCacheKey = contentHash(Buffer.from(JSON.stringify(pwaAssetManifest)));

mkdirSync(dirname(manifestFile), { recursive: true });
writeFileSync(
  manifestFile,
  [
    "// This file is generated by scripts/sync-pwa-assets.mjs.",
    `export const pwaAssetCacheKey = ${JSON.stringify(pwaAssetCacheKey)};`,
    `export const pwaAssetManifest = ${JSON.stringify(pwaAssetManifest, null, 2)} as const;`,
    "",
  ].join("\n"),
);

const serviceWorkerFile = join(publicDir, "sw.js");
const serviceWorker = readFileSync(serviceWorkerFile, "utf8")
  .replace(
    /const PWA_ASSET_CACHE_KEY = "[^"]+";/u,
    `const PWA_ASSET_CACHE_KEY = ${JSON.stringify(pwaAssetCacheKey)};`,
  )
  .replace(
    /const PWA_ASSET_MANIFEST = \{[\s\S]*?\};/u,
    `const PWA_ASSET_MANIFEST = ${JSON.stringify(pwaAssetManifest, null, 2)};`,
  );

writeFileSync(serviceWorkerFile, serviceWorker);
